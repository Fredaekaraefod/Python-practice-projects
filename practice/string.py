course = "py for newbies"
print(course.upper())  # returns in upper case
print(course.lower())  # returns in lower case
print(course.find("Y"))  # used to find the index of  the character 'Y'
print(course.find("y"))  # used to find the index of the character 'y'
print(course.find("for"))  # used to find the index of the character 'for'
print(course.replace("for", "4"))  # used to replace the character 'for' with '4'
print("python" in course)  # an alternative to .find() function
